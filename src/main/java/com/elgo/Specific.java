package com.elgo;

public class Specific {

    public int Plus(int a, int b) {
        return a + b;
    }

    public int Moins(int a, int b) {
        return a - b;
    }

    public int Mult(int a, int b) {
        return a * b;
    }
    public int Div(int a, int b) {
        return a / b;
    }
}
